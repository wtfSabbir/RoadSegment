"""
Inference Script: Segment New LAZ Tiles with Trained SegFormer
=================================================================
Step 4 of the curb-gutter segmentation pipeline.

What this script does:
  1. Reads config.json from --model_folder (auto-generated by train_segformer.py)
  2. Loads best.pt using the SAME build_model() function training used
     (imported from model_arch.py — guaranteed architecture match)
  3. Rasterizes new unlabeled LAZ tiles using the SAME settings training used
     (resolution, k-neighbors — read from config.json, not re-typed)
  4. Runs sliding-window inference with logit-averaging in overlap regions
  5. Back-projects predictions to 3D: Option A — only the highest-z point per
     pixel gets the predicted label directly, then a nearest-neighbor pass
     fills the remaining points in 3D space
  6. Writes predictions into the LAZ `classification` field
  7. Saves a 2D segmentation PNG + an RGB-overlay PNG for visual QA

Usage (folder mode):
  python inference.py \\
    --model_folder /path/to/checkpoints/Test_Run1_512Patches/ \\
    --input_folder /path/to/new/unlabeled/laz/ \\
    --output_folder /path/to/segmented/output/

Usage (single file mode):
  python inference.py \\
    --model_folder /path/to/checkpoints/Test_Run1_512Patches/ \\
    --single_file /path/to/one_tile.laz \\
    --output_folder /path/to/segmented/output/

All settings (resolution, k, patch_size, channel order, model architecture)
are read automatically from config.json inside --model_folder. You do NOT
need to re-specify them — this is the whole point of the config system.

If --input_folder / --single_file / --output_folder are omitted, the script
falls back to the inference_config values stored in config.json, so you can
also just edit config.json by hand and run with only --model_folder.

Dependencies:
  pip install torch torchvision transformers
  pip install laspy lazrs-python numpy scikit-learn opencv-python tqdm
"""

